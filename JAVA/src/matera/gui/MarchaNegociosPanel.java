    private void calcularButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularButtonActionPerformed
        if (!checkRequiredFields()){
            Utiles.showMessage("faltan rellenar algunos campos");
            return;
        } 
        
        // siempre es el primero del mes
        Date fromDate = java.sql.Date.valueOf(LocalDate.of(yearField.getYear(), monthField.getMonth()+1, 1));
        // fecha de cierre
        Date toDate = new java.sql.Date(this.to.getDate().getTime());
        // zona
        Integer id_zona = ComboBoxMgr.getSelectedId(zonaField);
               
        factTotalField.setValue(MarchaNegocioService.getTotalFacturado(fromDate, toDate, id_zona));
        factPendienteField.setValue(MarchaNegocioService.getPendienteFacturacionFinalizado(fromDate, toDate, id_zona));
        cxPendienteField.setValue(MarchaNegocioService.getMontoPresupuestadoConfirmadoConFechaCirugia(fromDate, toDate, id_zona));
        rmField.setValue(MarchaNegocioService.getRmPendiente(fromDate, toDate, id_zona));
        costoVentaField.setValue(MarchaNegocioService.getCostoVenta(fromDate, toDate, id_zona));
        ocSinFechaField.setValue(MarchaNegocioService.getMontoPresupuestadoAprobadoSinFechaCirugia(fromDate, toDate, id_zona));
        pendienteVipField.setValue(MarchaNegocioService.getPendientesVip(id_zona));
    }//GEN-LAST:event_calcularButtonActionPerformed

    private void guardarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarButtonActionPerformed
        marcha = MarchaNegocioService.getMarchaNegociosByYearAndMonth(yearField.getYear(), monthField.getMonth());

        // Escenario 1: todavia no hay una marcha de negocios para este mes y año
        if (marcha == null){
            Double iva = NumberUtils.toDouble(ivaField.getValue().toString(), 0.0);
            BigDecimal forecast = (BigDecimal) forecastField.getValue();
            if (iva == 0.0 || forecast.doubleValue() == 0.0){
                Utiles.showMessage("Faltan valores de IVA/Forecast");
                return;
            }
            
            marcha = new MarchaNegocios(null,
                    monthField.getMonth(),
                    yearField.getYear(),
                    iva,
                    forecast);
        }
    }//GEN-LAST:event_guardarButtonActionPerformed



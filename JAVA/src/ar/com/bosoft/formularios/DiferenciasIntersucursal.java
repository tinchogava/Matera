    private void setJTexFieldChanged(JTextField txt){
        DocumentListener documentListener = new DocumentListener() {
 
            @Override
            public void changedUpdate(DocumentEvent documentEvent) {
                printIt(documentEvent);
            }
 
            @Override
            public void insertUpdate(DocumentEvent documentEvent) {
                printIt(documentEvent);
            }

            @Override
            public void removeUpdate(DocumentEvent documentEvent) {
                printIt(documentEvent);
            }
        };
        txt.getDocument().addDocumentListener(documentListener); 
    }
 
    private void printIt(DocumentEvent documentEvent) {
        DocumentEvent.EventType type = documentEvent.getType();
 
        if (type.equals(DocumentEvent.EventType.CHANGE)){
 
        }else if (type.equals(DocumentEvent.EventType.INSERT)){
            JTxtBuscaChanged();
        }else if (type.equals(DocumentEvent.EventType.REMOVE)){
            JTxtBuscaChanged();
        }
    }
 
    private void JTxtBuscaChanged(){
        String text = jTxtBusca.getText().trim().replaceAll("'", "´");
        if (text.length() == 0) {
          sorterConsulta.setRowFilter(null);
        } else {
          sorterConsulta.setRowFilter(RowFilter.regexFilter("(?i).*" +text+ ".*"));
          jTablaConsulta.setRowSorter(sorterConsulta);
        }
    }
    
    private void consulta(){
        this.modeloConsulta.getDataVector().removeAllElements();
        this.modeloConsulta.fireTableDataChanged();
        
        try {
            ArrayList parametros = new ArrayList();
            
            parametros.add(this.jDesde.getDate() == null ? (long) 0 : this.jDesde.getDate().getTime());
            parametros.add(this.jHasta.getDate() == null ? (long) 0 : this.jHasta.getDate().getTime());
            parametros.add(this.id_empresa);
            
            this.rsConsulta = this.conexion.procAlmacenado("consultaDiferenciasIntersucursal", parametros,
                    this.empresa, this.getClass().getName(), Thread.currentThread().getStackTrace()[1].toString());
            
            if (rsConsulta.first()) {
                rsConsulta.beforeFirst();
                while (rsConsulta.next()) {
                    Date fecha = rsConsulta.getDate("fecha");
                    String numComp = rsConsulta.getString("numComp");
                    String origen = rsConsulta.getString("origen");
                    String destino = rsConsulta.getString("destino");
                    String codigo = rsConsulta.getString("codigo");
                    String nombre = rsConsulta.getString("nombre");
                    int diferencia = rsConsulta.getInt("diferencia");
                    String usuario = rsConsulta.getString("usuario");
                    String fechaRecepcion = rsConsulta.getString("fechaRecepcion");
                    
                    Object[] fila = {fecha, numComp, origen, destino, codigo,
                                        nombre, diferencia, usuario, fechaRecepcion};
                    
                    modeloConsulta.addRow(fila);
                }
            }else{
                JOptionPane.showMessageDialog(this, "No hay resultados para mostrar", "Atención", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException | HeadlessException ex) {
            Utiles.enviaError(this.empresa, ex);
        }
    }
    
    private void imprime(int salida){
        Map param = new HashMap();
        param.put("empresa", this.empresa);
        param.put("desde", this.jDesde.getDate());
        param.put("hasta", this.jHasta.getDate());
        
        Reporte reporte = new Reporte();
        reporte.startReport("DiferenciasIntersucursal", param, new JRTableModelDataSource(modeloConsulta), salida, rutaArchivo, seleccionImp.getImpresora(), seleccionImp.getCopias());
    }
    

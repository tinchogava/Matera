    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         if (validaObligatorios()){
            TipooperacionRecord tipoOpRecord = new TipooperacionRecord();
            tipoOpRecord.attach(ActiveDatabase.getDSLContext().configuration());
            tipoOpRecord.setIdTipooperacion(null);
            tipoOpRecord.setNombre(jTxtNombre.getText());
            tipoOpRecord.setHabilita(this.jComboEn.getSelectedItem().toString().substring(0, 1));
            
            TipoOpMgr.storeOp(tipoOpRecord);
            
            this.limpia();
            this.jTxtNombre.requestFocus();
        }
         
        modeloTipoOp.getRowsAsList()
                 .forEach(tOp->{
                     if(tOp.changed()){
                         tOp.store();
                     }
                 });
        
        modeloTipoOp.removeAllRows();
        
        TipoOpMgr.getTipoOp()
                .forEach(p->
                        modeloTipoOp.addRow(p)
                );
    }//GEN-LAST:event_jButton1ActionPerformed

